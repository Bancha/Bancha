/**
 * Tests the transform() method for extUpload actions.
 *
 */
	public function testTransformExtUploadAction() {
		// Response generated by CakePHP.
		$cakeResponse = array(
			'Article'	=> array(
				'id'	=> 304,
				'title'	=> 'foo',
			),
		);

		$request = new CakeRequest();
		$request->addParams(array(
			'controller'	=> 'Articles',
			'action'		=> 'add',
			'extUpload'		=> true,
		));

		// Response expected by Ext JS.
		$expectedResponse = '<html><body><textarea>' . str_replace('"', '\"', json_encode(array(
			'id'	=> 304,
			'title'	=> 'foo',
		))) . '</textarea></body></html>';

		$this->assertEquals($expectedResponse, BanchaResponseTransformer::transform($cakeResponse, $request));
	}

