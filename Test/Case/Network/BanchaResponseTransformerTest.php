class BanchaResponseTransformerTest extends CakeTestCase {

/**
 * Tests the transform() method for the 'index' action.
 *
 */
	public function testTransformIndexAction() {
		// Response generated by CakePHP
		$cakeResponse = array(
			array(
				'Article'	=> array(
					'id'	=> 304,
					'title'	=> 'foo',
				),
			),
			array(
				'Article'	=> array(
					'id'	=> 305,
					'title'	=> 'bar',
				),
			)
		);

		$request = new CakeRequest();
		$request->addParams(array(
			'controller'	=> 'Articles',
			'action'		=> 'index',
		));

		// Response expected by Ext JS (in JSON).
		$expectedResponse = array(
			array(
				'id'	=> 304,
				'title'	=> 'foo',
			),
			array(
				'id'	=> 305,
				'title'	=> 'bar',
			)
		);

		$this->assertEquals($expectedResponse, BanchaResponseTransformer::transform($cakeResponse, $request));
	}

